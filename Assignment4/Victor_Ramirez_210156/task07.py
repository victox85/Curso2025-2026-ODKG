"""**TASK 7.1b: Repeat the same exercise in SPARQL, returning the variables ?c (class) and ?sc (superclass)**"""

query =  """
            SELECT ?c ?sc
            WHERE {
              ?c a rdfs:Class .
              OPTIONAL {
                ?c rdfs:subClassOf ?sc .
              }
            }
"""

